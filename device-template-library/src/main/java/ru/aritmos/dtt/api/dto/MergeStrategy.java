package ru.aritmos.dtt.api.dto;

/**
 * Стратегия разрешения конфликтов при сборке профиля оборудования из нескольких шаблонов DTT.
 */
public enum MergeStrategy {
    /** Завершить сборку ошибкой при обнаружении существующего типа устройства с тем же ключом. */
    FAIL_IF_EXISTS,
    /** Полностью заменить существующий тип устройства новым. */
    REPLACE,
    /** Объединить только непустые значения из входящего шаблона в целевой тип. */
    MERGE_NON_NULLS,
    /** Объединить значения, сохраняя уже существующие непустые данные. */
    MERGE_PRESERVE_EXISTING,
    /** Создать копию конфликтующего типа устройства с числовым суффиксом. */
    CREATE_COPY_WITH_SUFFIX
}
